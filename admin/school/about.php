                <h1 class="mbr-section-title mbr-fonts-style mb-4 display-4"><strong>How It All Begun</strong></h1>
                <p class="mbr-text mbr-fonts-style mb-4 display-8">
                  St. Joseph’s begun due to a dream of founder Bishop Emeritus J.B Kakubi to train young Catholics who would in the future become responsible future leaders. Support was mobilized from donors notably the White Fathers from Missionaries of Africa. The missionaries were determined to help because of “the idea of Crafts”. Vocational courses were to be offered at the school. St. Joseph’s was to be a legacy of the Missionaries. The year 1980 marked the birth of St. Joseph’s. The founders gave St. Joseph’s Vocational School a mission “To educate young catholic boys, with the aim of equipping them with Christian leadership knowledge”. With a population of fewer than 100 students, the Missionaries of Africa were the first to help and these included Fr. Bruno Loiselle, Fr. Rodolf Lehnertz, Fr. Alex You (RIP), and Fr. George Vollinger(RIP). Everyone tried to give the best of service so as to get the best. Building, woodwork, and agriculture were the main subjects. Other subjects were introduced so as to match the demands of Uganda’s education system. The school started with O’level with the acquisition of UNEB center number UO346. Come the year 1983, the pioneer S.4 candidates sat their exams. St. Josephs was granted A‘level status in 1984 and two years later, A‘level pioneers sat for their first UNEB exams. Over the years, St Joseph’s developed the necessary infrastructure observed an increased number of teachers and students.<br>
